a = int(input("Digite o primeiro valor:"))
b = int(input("Digite o segundo valor:"))

resultado_soma = a + b
print ("Resultado da soma:", resultado_soma)

resultado_diminuição = a - b
print ("Resultado da diminuição:" , resultado_diminuição)

resultado_multiplicação = a * b
print ("Resultado da multiplicação:" , resultado_multiplicação)

resultado_divisão = a / b
print ("Resultado da divisão:" , resultado_divisão)